package org.openmbee.mdm.framework.codegen

import java.nio.file.Path

/**
 * Configuration for the TypeScript code generator.
 */
data class TypeScriptCodeGenConfig(
    /**
     * Output directory for generated TypeScript files.
     */
    val outputDir: Path,

    /**
     * Name of the metamodel (e.g., "KerML", "SysML").
     * Used for naming generated files (e.g., "kerml.model.ts").
     */
    val metamodelName: String = "KerML",

    /**
     * Whether to generate JSDoc comments from descriptions.
     */
    val generateDocs: Boolean = true,

    /**
     * File header comment (copyright, license, etc.).
     */
    val fileHeader: String = DEFAULT_FILE_HEADER,

    /**
     * List of class names to exclude from code generation.
     */
    val excludeClasses: Set<String> = emptySet()
) {
    companion object {
        val DEFAULT_FILE_HEADER = """
            // Auto-generated from KerML metamodel â€” DO NOT EDIT
            // Generated by TypeScriptCodeGenerator from gearshift-kerml-service
        """.trimIndent()
    }

    /**
     * Check if a class should be generated.
     */
    fun shouldGenerate(className: String): Boolean {
        return className !in excludeClasses
    }

    /**
     * Get the model file name (e.g., "kerml.model.ts").
     */
    val modelFileName: String
        get() = "${metamodelName.lowercase()}.model.ts"

    /**
     * Get the metaclass type file name (e.g., "kerml-metaclass.type.ts").
     */
    val metaclassTypeFileName: String
        get() = "${metamodelName.lowercase()}-metaclass.type.ts"
}
